import type { Transaction } from '@/types'

const now = new Date()
const year = now.getFullYear()
const month = now.getMonth() // 0-indexed, June = 5

/**
 * Helper to build ISO date strings relative to the current date.
 * Spreads transactions across the last 3 months.
 */
function dateRelative(monthsAgo: number, day: number, hour = 10, min = 0): string {
  const d = new Date(year, month - monthsAgo, day, hour, min, 0)
  return d.toISOString()
}

